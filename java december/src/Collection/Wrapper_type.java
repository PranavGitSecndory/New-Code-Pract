package Collection;

public class Wrapper_type {
public static void main(String[] args) {
	int i=100;
	Integer inte=i;//wrapper class integer
	System.out.println(inte);
	
	char ch='A';
	Character c=ch;//wrapper class character
	System.out.println(c);
	
	boolean a=true;
	Boolean b=a;//wrapper class boolean
	System.out.println(b);
	
}
}
