package Tutorial_Data;

public class Tutorial_Data {
    protected void Static1() {
        System.out.println("Static: Shared among all instances of a class.");
    }

    protected void Class() {
        System.out.println("Class: Blueprint to create objects.");
    }

    protected void Final() {
        System.out.println("Final: Used to declare constants or prevent inheritance/overriding.");
    }

    protected void Private() {
        System.out.println("Private: Restricts access to within the class.");
    }

    protected void Interface() {
        System.out.println("Interface: A contract that defines methods for a class to implement.");
    }

    protected void Polymorphism() {
        System.out.println("Polymorphism: Allows different classes to be treated through a common interface.");
    }

    protected void Exit() {
        System.out.println("Exiting Default");
    }
}